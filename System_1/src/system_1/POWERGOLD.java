    private void EmpTab_SubmitActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_EmpTab_SubmitActionPerformed
        String empsubmit = EmpTab_JobNum.getText();
        if(empsubmit.isEmpty()){
            JOptionPane.showMessageDialog(null,"Job Number cannot be empty");
        }
        else{   
                try{
                    pst = cn.prepareStatement("INSERT INTO `service_report`(Job_Num, Serv_Stat, Serv_Date, Company_Name, Comp_Cont, Problem_comp, Serv_Pers, Work_Done, Recomms) VALUES (?,?,?,?,?,?,?,?,?)");
                    pst.setString(1, EmpTab_JobNum.getText());
                    pst.setString(2, EmpTab_JobStatus.getSelectedItem());
                    pst.setString(3, EmpTab_JobDate.getText());
                    pst.setString(4, EmpTab_CompName.getText());
                    pst.setString(5, EmpTab_CompCont.getText());
                    pst.setString(6, EmpTab_Problem.getText());
                    pst.setString(7, EmpTab_ServPersArea.getText());
                    pst.setString(8, EmpTab_WorkDone_Field.getText());
                    pst.setString(9, EmpTab_Recomms_Field.getText());
                    pst.executeUpdate();
                JOptionPane.showMessageDialog(null, "Data was entered successfully");
                }    
                catch(SQLException e){
                    JOptionPane.showMessageDialog(null, e.getMessage());
                }
        }
    }//GEN-LAST:event_EmpTab_SubmitActionPerformed

