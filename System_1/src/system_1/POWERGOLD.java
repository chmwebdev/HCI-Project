    public ArrayList<User> userList(){
            ArrayList<User> userList = new ArrayList<>();
            try{
                Class.forName("com.mysql.jdbc.Driver");
                cn = DriverManager.getConnection("jdbc:mysql://localhost:3306/powergold?zeroDateTimeBehavior=convertToNull","root","");
                st = cn.createStatement();
                pst = cn.prepareStatement("SELECT * FROM `employee_data` WHERE 1");
                rs = pst.executeQuery();
                User user;
                while(rs.next()){
                    user = new User(rs.getString("Employee_Num"), rs.getString("Employee_FirstName"), rs.getString("Employee_MiddleName"), rs.getString("Employee_LastName"), rs.getString("Username"), rs.getString("Password"), rs.getBoolean("Admin"));
                    userList.add(user);
                }
            }
            catch(ClassNotFoundException e){
                JOptionPane.showMessageDialog(null,"Class Not Found");
            }
            catch(SQLException e){
                JOptionPane.showMessageDialog(null,"SQL Not Found");
            }
            return userList;
        }
    
    public void show_user(){
        ArrayList<User> list = userList();
        DefaultTableModel model = (DefaultTableModel) AdminEmp_EmployeeTable.getModel();
        Object[] row = new Object[7];
        for(int i = 0; i<list.size(); i++){
            row[0] = list.get(i).getEmployee_Num();
            row[1] = list.get(i).getEmployee_FirstName();
            row[2] = list.get(i).getEmployee_MiddleName();
            row[3] = list.get(i).getEmployee_LastName();
            row[4] = list.get(i).getUserName();
            row[5] = list.get(i).getPassword();
            row[6] = list.get(i).getAdmin();
            model.addRow(row);
            
        }
    }
    
    public void update_user(){
        DefaultTableModel dtm = (DefaultTableModel) AdminEmp_EmployeeTable.getModel();
        dtm.setRowCount(0);
        show_user();
    }

